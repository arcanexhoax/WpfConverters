namespace WpfConverters
{
    /// <summary>
    /// Comparison operation between 2 values.
    /// </summary>
    public enum NumberComparisonOperation
    {
        /// <summary>
        /// Comparison operation: <
        /// </summary>
        Less,
        /// <summary>
        /// Comparison operation: <=
        /// </summary>
        LessOrEquals,
        /// <summary>
        /// Comparison operation: >
        /// </summary>
        More,
        /// <summary>
        /// Comparison operation: >=
        /// </summary>
        MoreOrEquals,
        /// <summary>
        /// Comparison operation: ==
        /// </summary>
        Equals,
        /// <summary>
        /// Comparison operation: ==
        /// </summary>
        NotEquals,
    }
}
